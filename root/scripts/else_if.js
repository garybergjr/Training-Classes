var value1 = 5;
var value2 = 7;
var value3 = 12;

if (value1 <= value2) {
  console.log(value1 + " is less than or equal to " + value2);
} else if (value1 >= value2) {
  console.log(value1 + " is greater than or equal to " + value2);
} else if (value1 + value2 == value3) {
  console.log (value1 + " + " + value2 + " is equal to " + value3);
}
